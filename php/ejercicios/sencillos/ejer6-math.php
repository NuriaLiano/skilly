<?php
// Ejemplo de uso de funciones matemáticas en PHP

// Número de prueba
$numero = 6.75;

// Redondeo hacia abajo
$redondeoAbajo = floor($numero);

// Redondeo hacia arriba
$redondeoArriba = ceil($numero);

// Valor absoluto
$valorAbsoluto = abs($numero);

// Potencia
$potencia = pow($numero, 2);

// Raíz cuadrada
$raizCuadrada = sqrt($numero);

// Seno
$seno = sin($numero);

// Coseno
$coseno = cos($numero);

// Valor máximo
$maximo = max(2, 8, 5, 9, 3);

// Valor mínimo
$minimo = min(2, 8, 5, 9, 3);

// Imprimir los resultados
echo "Número: $numero <br>";
echo "Redondeo hacia abajo: $redondeoAbajo <br>";
echo "Redondeo hacia arriba: $redondeoArriba <br>";
echo "Valor absoluto: $valorAbsoluto <br>";
echo "Potencia: $potencia <br>";
echo "Raíz cuadrada: $raizCuadrada <br>";
echo "Seno: $seno <br>";
echo "Coseno: $coseno <br>";
echo "Valor máximo: $maximo <br>";
echo "Valor mínimo: $minimo <br>";
?>
